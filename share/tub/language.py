
class Language(object):
    """
    A language that the application can be configured to support.

    Each language has the following attributes:
    
      * code: a 2-character country code, i.e. 'en', 'fr'
      * name: the English name, i.e. "English", "French", etc.
      * nativeName: the language's name in the native language, i.e. "English",
            "Francais" (with a c-cedilla), etc.
      * rightToLeft: Boolean to signify that the language is rtl.
    """

    def __init__(self, code, name, nativeName, rightToLeft=False):
        self.code = code
        self.name = name
        self.nativeName = nativeName
        self.rightToLeft = rightToLeft
